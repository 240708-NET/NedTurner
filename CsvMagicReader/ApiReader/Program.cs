
using CsvReader.Repo;

namespace ApiReader;

public class Program
{
    public static void Main(string[] args)
    {
        var builder = WebApplication.CreateBuilder(args);

        // Add services to the container.

        builder.Services.AddControllers();
        // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
        builder.Services.AddEndpointsApiExplorer();
        builder.Services.AddSwaggerGen();
        builder.Services.AddScoped<IRepo>(repo => new SqlRepo());
        // builder.Services.AddScoped<IRepo>(repo => new SqlRepo(builder.Configuration.GetConnectionString("Database")));
        // builder.Services.AddScoped<IRepo>(repo => new SqlRepo(File.ReadAllText(@"../CsvReader.Repo/connectionstring")));

        var app = builder.Build();

        // Configure the HTTP request pipeline.
        if (app.Environment.IsDevelopment())
        {
            app.UseSwagger();
            app.UseSwaggerUI();
        }

        app.UseHttpsRedirection();

        app.UseAuthorization();


        app.MapControllers();

        app.Run();
    }
}
